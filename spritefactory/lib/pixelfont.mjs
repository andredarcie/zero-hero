export const GLYPH_W = 5;
export const GLYPH_H = 7;
const TRACK = 1; // vao entre letras, em px de fonte

/** Largura em px de uma string nesta fonte, na escala dada. */
export const textWidth = (str, scale = 1) =>
  (str.length * (GLYPH_W + TRACK) - TRACK) * scale;

/**
 * Escreve `str` no canvas em (x, y), cada pixel de fonte virando um quadrado `scale`×`scale`.
 * `plot(px, py)` recebe pixels JA em coordenadas do canvas — quem chama decide a cor.
 */
export const drawText = (str, x, y, scale, plot) => {
  let cx = x;
  for (const ch of str.toUpperCase()) {
    const glyph = G[ch] ?? G['?'] ?? G[' '];
    for (let gy = 0; gy < GLYPH_H; gy += 1) {
      for (let gx = 0; gx < GLYPH_W; gx += 1) {
        if (glyph[gy][gx] !== '#') continue;
        for (let sy = 0; sy < scale; sy += 1) {
          for (let sx = 0; sx < scale; sx += 1) plot(cx + gx * scale + sx, y + gy * scale + sy);
        }
      }
    }
    cx += (GLYPH_W + TRACK) * scale;
  }
};
